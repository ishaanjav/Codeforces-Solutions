int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;

    while (t--) {
        int n;
        cin >> n;
        int ar[n];
        for (int& x : ar) cin >> x;

        int a = -1, b, c;
        for (int i = 0; i < n - 2; i++) {
            if (ar[i] < ar[i + 1] && ar[i + 1] > ar[i + 2]) {
                a = i, b = i + 1, c = i + 2;
                break;
            }
        }
        a++, b++, c++;
        if (a == 0)
            cout << "NO\n";
        else
            cout << "YES\n"
                 << a << " " << b << " " << c << endl;
    }
}
