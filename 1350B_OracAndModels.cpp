int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        ll ar[n + 1];
        ll dp[n + 1];
        for (int tr = 1; tr <= n; tr++) {
            cin >> ar[tr];
            dp[tr] = 1;
        }
        ll ans = 1;
        for (int i = 1; i <= n; i++)
            for (int j = i * 2; j <= n; j += i)
                if (ar[i] < ar[j]) {
                    max_self(dp[j], dp[i] + 1);
                    max_self(ans, dp[j]);
                }
        cout << ans << endl;
    }
}
