int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int n, k;
    cin >> n >> k;
    ll ar[n];
    ll biggest = -1e9;
    ll smallest = 1e9;
    for (int tr = 0; tr < n; tr++) {
        cin >> ar[tr];
        max_self(biggest, ar[tr]);
        min_self(smallest, ar[tr]);
    }

    if (k >= 3) {
        cout << biggest;
        return 0;
    } else if (k == 1) {
        cout << smallest;
        return 0;
    }

    cout << max(ar[0], ar[n - 1]);
}
